public class Main2 {
    public static void main(String[] args) {
        //除法
        System.out.println(10 / 2);//5
        System.out.println(10 / 3);//3
        System.out.println(10.0 / 3);//3.3333333333333335
        //结论：1.整数参与计算，结果只能是整数
        //2.小数参与计算结果有可能不精确。

        //取模 也是做除法，不过结果为余数
        System.out.println(10 % 2);//0
        System.out.println(10 % 3);//1


    }
}
